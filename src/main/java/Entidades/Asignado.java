package Entidades;

public class Asignado {
    private int ci;
    private int id;
    public Asignado(int ci, int id){
        this.ci = ci;
        this.id = id;
    }

    public int getCi() {
        return ci;
    }

    public void setCi(int ci) {
        this.ci = ci;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
